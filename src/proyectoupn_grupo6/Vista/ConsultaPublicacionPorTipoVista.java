    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        try {

            model.setRowCount(0);
            String idTipo = cboTipo.getSelectedItem().toString().split("-")[0].trim();
            String idPublicacion = txtIdPublicacion.getText().trim();
            List<Publicacion> publicaciones =  controller.listarPublicacionPorTipo(idTipo, idPublicacion);
            for (int i = 0; i < publicaciones.size(); i++) {
                Object[] row = new Object[]{publicaciones.get(i).getIdPublicacion(),
                    publicaciones.get(i).getTitulo(),
                    publicaciones.get(i).getIdTipo(),
                    publicaciones.get(i).getNroEdicion(),
                    publicaciones.get(i).getAutor(),
                    publicaciones.get(i).getPrecio()
                    , publicaciones.get(i).getStock()};
                model.addRow(row);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error : " + e.getMessage());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnConsultarActionPerformed

