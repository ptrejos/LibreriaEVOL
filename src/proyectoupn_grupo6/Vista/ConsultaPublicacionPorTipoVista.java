    private void cargarComboTipo()throws SQLException {
        cboTipo.removeAllItems();
        List<Tipo> tipos = controller.listarTipos();
        for (int i = 0; i < tipos.size(); i++) {
           cboTipo.addItem(tipos.get(i).getIdTipo()+ " - " + tipos.get(i).getDescripcion());
        }
    }
